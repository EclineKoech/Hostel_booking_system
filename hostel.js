$(document).ready(function () {  
  $("div#image").click(function(event) {      
      event.preventDefault();      
  $("div#image").toggle();     
  $("p#MADARAKA").toggle(); 
  $("div#image").hide();
  });
  $("p#MADARAKA").click(function(event) {  
   event.preventDefault();       
   $("p#MADARAKA").toggle();      
   $("div#image").toggle();      
   $("div#image").show();
    });});
